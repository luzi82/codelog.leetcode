class Solution {
    fun countBattleships(board: Array<CharArray>): Int {
        val ii=board.size
        val jj=board[0].size

        var ret=0
        for(i in 0..(ii-1)){
          for(j in 0..(jj-1)){
            if(board[i][j]!='X'){continue}
            
          }
        }

        return ret
    }
}
